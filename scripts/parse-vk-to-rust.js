/**
 * scripts/parse-vk-to-rust.js
 *
 * Converts snarkjs `verification_key.json` → Rust source at
 * `programs/sakura-insurance/src/zk_verifying_key.rs`.
 *
 * Format is compatible with the `groth16-solana` crate (Light Protocol fork),
 * which expects big-endian 32-byte limbs for every BN254 field element.
 *
 * Also negates the G1 alpha point (per the "prepared" Groth16 convention
 * used by groth16-solana — negation of alpha_1 lets the verifier do ONE
 * pairing check instead of two).
 *
 * Usage:
 *   node scripts/parse-vk-to-rust.js
 */
const fs = require("fs");
const path = require("path");
const ffjavascript = require("ffjavascript");

const VK_JSON = path.join(__dirname, "..", "circuits", "build", "verification_key.json");
const OUT_RS = path.join(
  __dirname,
  "..",
  "programs",
  "sakura-insurance",
  "src",
  "zk_verifying_key.rs"
);

// BN254 curve modulus p
const P =
  21888242871839275222246405745257275088696311157297823662689037894645226208583n;

function toBE32(nStr) {
  // Convert decimal string → 32-byte big-endian hex → Rust u8 array
  let n = BigInt(nStr);
  if (n < 0n) n = (n + P) % P;
  const hex = n.toString(16).padStart(64, "0");
  const bytes = [];
  for (let i = 0; i < 64; i += 2) {
    bytes.push(`0x${hex.slice(i, i + 2)}`);
  }
  return bytes;
}

function g1ToBytes(g1) {
  // g1 = [x, y, "1"] — take x, y as BE32, concatenated → 64 bytes
  return [...toBE32(g1[0]), ...toBE32(g1[1])];
}

function g2ToBytes(g2) {
  // g2 = [[x.c0, x.c1], [y.c0, y.c1], ["1","0"]]
  // snarkjs stores c0/c1 in *reversed* order compared to ark-bn254 serialization
  // used by groth16-solana; we swap to match (c1, c0).
  return [
    ...toBE32(g2[0][1]),
    ...toBE32(g2[0][0]),
    ...toBE32(g2[1][1]),
    ...toBE32(g2[1][0]),
  ];
}

function negateG1(g1) {
  // negate y coordinate mod p
  const x = BigInt(g1[0]);
  const y = BigInt(g1[1]);
  const negY = (P - y) % P;
  return [x.toString(), negY.toString(), "1"];
}

function formatBytes(bytes, indent = "        ") {
  // Format as 8-byte rows
  const rows = [];
  for (let i = 0; i < bytes.length; i += 8) {
    rows.push(indent + bytes.slice(i, i + 8).join(", ") + ",");
  }
  return rows.join("\n");
}

function main() {
  const vk = JSON.parse(fs.readFileSync(VK_JSON, "utf8"));
  if (vk.protocol !== "groth16") {
    throw new Error(`Expected protocol "groth16", got ${vk.protocol}`);
  }
  if (vk.curve !== "bn128") {
    throw new Error(`Expected curve "bn128", got ${vk.curve}`);
  }

  const nPublic = vk.nPublic;
  const icLen = vk.IC.length;
  if (icLen !== nPublic + 1) {
    throw new Error(`IC length ${icLen} != nPublic + 1 (${nPublic + 1})`);
  }

  // IMPORTANT: do NOT negate alpha_1. Light Protocol's groth16-solana
  // crate expects the VK's alpha_g1 to be the POSITIVE trusted-setup value.
  // The single-pairing form of Groth16 is balanced by negating `proof_a`
  // on the caller side (see `proofToOnchainBytes` in lib/zk-proof.ts and
  // the crate's own functional test at src/groth16.rs which calls
  // `proof_a.neg()` before feeding the verifier). Pre-negating alpha here
  // combined with the caller's proof_a negation produces double-negation
  // and breaks the pairing check — empirically verified on devnet
  // 2026-04-19 (116k CU pairing failure).
  const alphaBytes = g1ToBytes(vk.vk_alpha_1);
  const betaBytes = g2ToBytes(vk.vk_beta_2);
  const gammaBytes = g2ToBytes(vk.vk_gamma_2);
  const deltaBytes = g2ToBytes(vk.vk_delta_2);
  const icBytes = vk.IC.map((pt) => g1ToBytes(pt));

  const icRows = icBytes
    .map(
      (bytes, i) =>
        `    // IC[${i}]\n    [\n${formatBytes(bytes, "        ")}\n    ],`
    )
    .join("\n");

  const out = `// AUTO-GENERATED by scripts/parse-vk-to-rust.js — DO NOT EDIT BY HAND.
//
// Source: circuits/build/verification_key.json (Groth16, BN254, nPublic=${nPublic})
// Encoding: big-endian 32-byte limbs, compatible with groth16-solana v0.2.
//
// Public inputs (in order, must match circom main's \`public\` list):
//   [0] commitment_hash       — Poseidon(collateral, debt, wallet, nonce)
//   [1] trigger_hf_bps        — e.g. 10_500 ⇒ HF < 1.05
//   [2] rescue_amount_bucket  — buckets of 100 USDC
//
// Balance of Groth16 pairing: alpha_g1 is stored POSITIVE here; the caller
// of Groth16Verifier is expected to pass a NEGATED proof_a (see the crate's
// own src/groth16.rs tests which use proof_a.neg() and POSITIVE VK alpha).

use groth16_solana::groth16::Groth16Verifyingkey;

pub const VERIFYINGKEY: Groth16Verifyingkey = Groth16Verifyingkey {
    nr_pubinputs: ${nPublic},

    // alpha_1 (POSITIVE — caller negates proof_a to balance the pairing)
    vk_alpha_g1: [
${formatBytes(alphaBytes, "        ")}
    ],

    vk_beta_g2: [
${formatBytes(betaBytes, "        ")}
    ],

    vk_gamme_g2: [
${formatBytes(gammaBytes, "        ")}
    ],

    vk_delta_g2: [
${formatBytes(deltaBytes, "        ")}
    ],

    vk_ic: &[
${icRows}
    ],
};
`;

  fs.mkdirSync(path.dirname(OUT_RS), { recursive: true });
  fs.writeFileSync(OUT_RS, out);
  console.log(`✅ wrote ${OUT_RS} (${out.length} bytes, ${icLen} IC points)`);
}

main();
